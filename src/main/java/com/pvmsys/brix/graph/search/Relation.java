package com.pvmsys.brix.graph.search;

public class Relation
{
   public String elementnameFrom ;
   public String elementnameTo ;
   public String relationname ;
   
   public String getFromElementName()
	{
		return elementnameFrom;
	}
   
   public String getToElementName()
  	{
  		return elementnameTo;
  	}
   
	public String getName()
	{
		return relationname;
	}
	public String getJoin()
	{
		return relationname;
	}

}
