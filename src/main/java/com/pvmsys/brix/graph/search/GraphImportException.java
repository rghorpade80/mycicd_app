package com.pvmsys.brix.graph.search;

public class GraphImportException extends Exception {

	/**
	 * 
	 */
	private static final long serialVersionUID = 1355425126632901370L;

	public GraphImportException() {
		super();
	}

	public GraphImportException(String message, Throwable cause, boolean enableSuppression, boolean writableStackTrace) {
		super(message, cause, enableSuppression, writableStackTrace);
	}

	public GraphImportException(String message, Throwable cause) {
		super(message, cause);
	}

	public GraphImportException(String message) {
		super(message);
	}

	public GraphImportException(Throwable cause) {
		super(cause);
	}

}
